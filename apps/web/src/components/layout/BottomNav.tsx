const navItems: NavItem[] = [
  { label: "トップ", href: "/", icon: <HomeIcon /> },
  { label: "承認", href: "/approval", icon: <CheckIcon /> },
  { label: "入力", href: "/input", icon: <PlusIcon />, isCenter: true },
  { label: "レポート", href: "/report", icon: <ChartIcon /> },
  { label: "設定", href: "/settings", icon: <GearIcon /> },
];

export default function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-white border-t border-border max-w-[430px] mx-auto pb-[env(safe-area-inset-bottom)]">
      <div className="flex items-end justify-around h-16">
        {navItems.map((item) => {
          const isActive = pathname === item.href;

          if (item.isCenter) {
            return (
              <Link
                key={item.href}
                href={item.href}
                className="flex flex-col items-center justify-center -mt-4 no-underline"
              >
                <span className="w-14 h-14 flex items-center justify-center rounded-full bg-gold text-white shadow-lg">
                  {item.icon}
                </span>
                <span className="text-[10px] mt-0.5 text-gold font-medium">
                  {item.label}
                </span>
              </Link>
            );
          }

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex flex-col items-center justify-center gap-0.5 py-2 no-underline ${
                isActive ? "text-gold" : "text-sub"
              }`}
            >
              {item.icon}
              <span className="text-[10px] font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
